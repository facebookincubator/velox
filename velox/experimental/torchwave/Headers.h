    "// output 'out'; 'idx' is the logical row-major index of one output element. The\n"
    "// index_select result has 'source's shape with dimension 'dim' resized to the\n"
    "// index length; that result broadcasts to 'out'. Decompose idx into per-dim\n"
    "// coordinates using out's dims, right-align the result within out\n"
    "// ('dimOffset'), broadcast any size-1 result dimension to coordinate 0, replace\n"
    "// the coordinate along 'dim' with the selected index value, and read 'source'\n"
    "// at the resulting offset. This mirrors ATen's advanced-indexing kernel:\n"
    "//   offset = sum(coord[d] * source->strides[d], d != dim)\n"
    "//          + index[coord[dim]] * source->strides[dim]\n"
    "// (see ATen native/cuda/IndexKernel.cu). 'source' and 'index' are whole tensors\n"
    "// read at computed offsets (so they carry randomAccess and are materialized\n"
    "// before this op). An out-of-range index is reported like __index_elt_*.\n"
    "template <typename T, int32_t kDim>\n"
    "__device__ inline T __index_select(\n"
    "    uint32_t idx,\n"
    "    Tensor* source,\n"
    "    Tensor* index,\n"
    "    Tensor* out,\n"
    "    BlockInfo& block) {\n"
    "  int32_t dim = kDim < 0 ? kDim + source->rank : kDim;\n"
    "  // Decompose the logical output index into per-dim coordinates using the\n"
    "  // enclosing expression's output shape (innermost dim first). out->sizes[]\n"
    "  // hold out's own-dims magic dividers innermost-first (sizes[i] divides by\n"
    "  // out->dims[rank-1-i]); force-initialized in the block prologue.\n"
    "  int32_t coord[kMaxDims];\n"
    "  uint32_t rem = idx;\n"
    "  for (int i = 0; i < out->rank; ++i) {\n"
    "    unsigned int q, r;\n"
    "    out->sizes[i].divmod(rem, q, r);\n"
    "    coord[out->rank - 1 - i] = static_cast<int32_t>(r);\n"
    "    rem = q;\n"
    "  }\n"
    "  int32_t dimOffset = out->rank - source->rank;\n"
    "  // int64 offset (like ATen's IndexKernel) so coord*stride accumulation does\n"
    "  // not overflow for large tensors.\n"
    "  int64_t offset = 0;\n"
    "  int32_t pos = 0;\n"
    "  for (int d = 0; d < source->rank; ++d) {\n"
    "    int32_t oDimSize = d == dim ? index->dims[0] : source->dims[d];\n"
    "    int32_t c = oDimSize == 1 ? 0 : coord[d + dimOffset];\n"
    "    if (d == dim) {\n"
    "      pos = c;\n"
    "    } else {\n"
    "      offset += static_cast<int64_t>(c) * source->strides[d];\n"
    "    }\n"
    "  }\n"
    "  int64_t selected = index->elementType == kScalarTypeLong\n"
    "      ? storage<int64_t>(index)[pos * index->strides[0]]\n"
    "      : static_cast<int64_t>(storage<int32_t>(index)[pos * index->strides[0]]);\n"
    "  if (selected < 0) {\n"
    "    selected += source->dims[dim];\n"
    "  }\n"
    "  if (selected < 0 || selected >= source->dims[dim]) {\n"
    "    if (block.debugInfo) {\n"
    "      block.debugInfo->line = __LINE__;\n"
    "      block.debugInfo->extra[0] = dim;\n"
    "      block.debugInfo->extra[1] = selected;\n"
    "      SET_MSG(block.debugInfo, \"Bad idx\\0\");\n"
    "    }\n"
    "    return T();\n"
    "  }\n"
    "  offset += selected * source->strides[dim];\n"
    "  return storage<T>(source)[offset];\n"
    "}\n"
    "\n"
    "// Fused elementwise repeat (aten.repeat): tiles 'self' along each dim. This is\n"
    "// the same fused gather as __index_select, but the source coordinate along each\n"
    "// dim comes from a MODULO of the output coordinate instead of an index tensor.\n"
    "// The enclosing elementwise loop iterates every element of the whole\n"
    "// expression's output 'out'; 'idx' is the logical row-major index of one output\n"
    "// element. repeat's output dim d is repeats[d] * (rank-aligned self dim), and\n"
    "// len(repeats) may exceed self's rank, prepending leading 1-dims that all map\n"
    "// to self index 0. Decompose idx into per-dim coordinates using out's dims\n"
    "// (innermost first), right-align self within out ('dimOffset'), reduce each\n"
    "// output coordinate modulo the corresponding self dim (leading prepended dims\n"
    "// fall outside [dimOffset, out->rank) and contribute nothing), and read 'self'\n"
    "// at the resulting offset. 'self' is a whole tensor read at computed offsets\n"
    "// (randomAccess, materialized before this op).\n"
    "template <typename T>\n"
    "__device__ inline T\n"
    "__repeat(uint32_t idx, Tensor* self, Tensor* out, BlockInfo& /*block*/) {\n"
    "  // Decompose the logical output index into per-dim coordinates using the\n"
    "  // enclosing expression's output shape (innermost dim first). out->sizes[]\n"
    "  // hold out's own-dims magic dividers innermost-first (sizes[i] divides by\n"
    "  // out->dims[rank-1-i]); force-initialized in the block prologue.\n"
    "  int32_t coord[kMaxDims];\n"
    "  uint32_t rem = idx;\n"
    "  for (int i = 0; i < out->rank; ++i) {\n"
    "    unsigned int q, r;\n"
    "    out->sizes[i].divmod(rem, q, r);\n"
    "    coord[out->rank - 1 - i] = static_cast<int32_t>(r);\n"
    "    rem = q;\n"
    "  }\n"
    "  int32_t dimOffset = out->rank - self->rank;\n"
    "  // int64 offset (like __index_select) so coord*stride accumulation does not\n"
    "  // overflow for large tensors.\n"
    "  int64_t offset = 0;\n"
    "  for (int d = 0; d < self->rank; ++d) {\n"
    "    // self->sizes[self->rank-1-d] holds the magic divider for self->dims[d]\n"
    "    // (own-dims magic stored innermost-first); force-initialized in the block\n"
    "    // prologue. Wrap (tile) the output coordinate modulo self's dim.\n"
    "    unsigned int c = self->sizes[self->rank - 1 - d].mod(\n"
    "        static_cast<unsigned int>(coord[d + dimOffset]));\n"
    "    offset += static_cast<int64_t>(c) * self->strides[d];\n"
    "  }\n"
    "  return storage<T>(self)[offset];\n"
    "}\n"
    "\n"
    "// Binary-search core shared by bucketize and searchsorted. Searches the sorted\n"
    "// run data[start], data[start + stride], ..., data[start + (n-1)*stride] for\n"
    "// 'val' and returns the insertion position relative to the run start, in the\n"
    "// range [0, n]. Matches ATen's lower_bound / upper_bound\n"
    "// (native/cuda/Bucketization.cu): right=false is lower_bound (the first index\n"
    "// whose value is >= val); right=true is upper_bound (the first index whose\n"
    "// value is > val). Both advance on a negated comparison, so any comparison\n"
    "// against NaN is false: a NaN query lands at 'n' (past the run) and NaN run\n"
    "// entries sort as the largest values, exactly as in ATen. arithCast keeps the\n"
    "// comparison in float for half / bfloat16 operands.\n"
    "template <typename T>\n"
    "__device__ inline int64_t __binary_search(\n"
    "    const T* data,\n"
    "    int64_t start,\n"
    "    int64_t n,\n"
    "    int64_t stride,\n"
    "    T val,\n"
    "    bool right) {\n"
    "  int64_t lo = 0;\n"
    "  int64_t hi = n;\n"
    "  while (lo < hi) {\n"
    "    int64_t mid = lo + ((hi - lo) >> 1);\n"
    "    T midVal = data[start + mid * stride];\n"
    "    bool advance = right ? !(arithCast(midVal) > arithCast(val))\n"
    "                         : !(arithCast(midVal) >= arithCast(val));\n"
    "    if (advance) {\n"
    "      lo = mid + 1;\n"
    "    } else {\n"
    "      hi = mid;\n"
    "    }\n"
    "  }\n"
    "  return lo;\n"
    "}\n"
    "\n"
    "// Output integer width of a search op, selected at compile time from the op's\n"
    "// out_int32 flag (0 -> int64, 1 -> int32). Passed as a template argument so the\n"
    "// int32 and int64 variants compile as distinct, correctly-typed kernels. The\n"
    "// variants share query/searched dtypes, so without a template difference the\n"
    "// kernel dedup (subgraphNodesMatch) would merge them and store the wrong width.\n"
    "// The flag is an int (not bool) because it is emitted as a template argument\n"
    "// and bool constants stringify to \"True\"/\"False\", which are not valid C++.\n"
    "template <int OutInt32>\n"
    "struct SearchOutType {\n"
    "  using type = int64_t;\n"
    "};\n"
    "template <>\n"
    "struct SearchOutType<1> {\n"
    "  using type = int32_t;\n"
    "};\n"
    "\n"
    "// aten.bucketize.Tensor: 'val' is one query element and 'boundaries' the 1-D\n"
    "// sorted bucket edges (a whole tensor read at random). Returns the bucket index\n"
    "// in the requested output width (int64, or int32 when out_int32).\n"
    "template <typename T, int OutInt32>\n"
    "__device__ inline typename SearchOutType<OutInt32>::type\n"
    "__bucketize(T val, Tensor* boundaries, bool right) {\n"
    "  int32_t last = boundaries->rank - 1;\n"
    "  return static_cast<typename SearchOutType<OutInt32>::type>(__binary_search<T>(\n"
    "      storage<T>(boundaries),\n"
    "      0,\n"
    "      boundaries->dims[last],\n"
    "      boundaries->strides[last],\n"
    "      val,\n"
    "      right));\n"
    "}\n"
    "\n"
    "// aten.searchsorted.Tensor: 'val' is one query element of 'self' and 'sorted'\n"
    "// the sorted sequence (a whole tensor read at random). A 1-D 'sorted' is\n"
    "// searched in full by every query. For an N-D 'sorted' the search is per row:\n"
    "// its leading rank-1 dims match 'self' (== the enclosing loop output 'out'), so\n"
    "// the query at output element 'idx' searches the 'sorted' row picked by idx's\n"
    "// leading coordinates. This mirrors ATen's start_bd = tid / idim_in * idim_bd\n"
    "// (native/cuda/Bucketization.cu), generalized to strided rows via 'out's\n"
    "// per-dim dividers (as in __index_select).\n"
    "template <typename T, int OutInt32>\n"
    "__device__ inline typename SearchOutType<OutInt32>::type\n"
    "__searchsorted(uint32_t idx, Tensor* sorted, T val, bool right, Tensor* out) {\n"
    "  int32_t sr = sorted->rank;\n"
    "  int64_t start = 0;\n"
    "  if (sr > 1) {\n"
    "    int32_t coord[kMaxDims];\n"
    "    uint32_t rem = idx;\n"
    "    for (int i = 0; i < out->rank; ++i) {\n"
    "      unsigned int q, r;\n"
    "      out->sizes[i].divmod(rem, q, r);\n"
    "      coord[out->rank - 1 - i] = static_cast<int32_t>(r);\n"
    "      rem = q;\n"
    "    }\n"
    "    int32_t dimOffset = out->rank - sr;\n"
    "    for (int d = 0; d < sr - 1; ++d) {\n"
    "      start += static_cast<int64_t>(coord[d + dimOffset]) * sorted->strides[d];\n"
    "    }\n"
    "  }\n"
    "  return static_cast<typename SearchOutType<OutInt32>::type>(__binary_search<T>(\n"
    "      storage<T>(sorted),\n"
    "      start,\n"
    "      sorted->dims[sr - 1],\n"
    "      sorted->strides[sr - 1],\n"
    "      val,\n"
    "      right));\n"
    "}\n"
    "\n"
    "} // namespace torch::wave\n";
