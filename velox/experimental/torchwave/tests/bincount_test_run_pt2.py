from __future__ import annotations

import argparse
import os

import torch
from velox.experimental.torchwave.tests.bincount_test import BincountTestPreproc


def main() -> None:
    parser = argparse.ArgumentParser()
    default_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "data")
    parser.add_argument(
        "--output_dir",
        default=default_dir,
        help="Directory to write bincount_test.pt2 and bincount_test_results.pt",
    )
    args = parser.parse_args()
    output_dir = args.output_dir
    os.makedirs(output_dir, exist_ok=True)

    # small: max is 9 -> 10 bins (single-block final); minlength=16 pads to 16.
    small = torch.tensor([0, 3, 3, 3, 7, 7, 1, 5, 5, 9], dtype=torch.int64)
    # large: 50k elements over 100 bins -> multi-block/cg final with heavy
    # atomic-add contention.
    large = torch.arange(50000, dtype=torch.int64) % 100

    inputs = (small, large)

    module = BincountTestPreproc()
    results = module(*inputs)
    print(f"Eager results ({len(results)} outputs):")
    for i, r in enumerate(results):
        print(f"  [{i}] shape={tuple(r.shape)}, dtype={r.dtype}")

    results_path = os.path.join(output_dir, "bincount_test_results.pt")
    torch.save(list(results), results_path)
    print(f"Saved results to {results_path}")

    print("Exporting via torch.export...")
    with torch.no_grad():
        exported_program = torch.export.export(module, inputs, strict=False)
    print(f"Export successful, graph has {len(exported_program.graph.nodes)} nodes")

    pt2_path = os.path.join(output_dir, "bincount_test.pt2")
    print(f"Saving exported program to {pt2_path}")
    torch.export.save(exported_program, pt2_path)
    print(f"Successfully saved .pt2 to {pt2_path}")


if __name__ == "__main__":
    main()
